var express = require('express');
var apiRouter = express.Router();

var Pesticide = require('../../../businessObjects/pesticide.js');

apiRouter.get('/', function(req, res) {
  var onSuccess=function (err, data) {
    if (err) {console.log("error occured!");}
    res.json(data);
  };
  Pesticide.find(onSuccess);
});

apiRouter.get('/:id', function(req, res) {
    var onSuccess=function (err, data) {
    if (err) {console.log("error occured!");}
    res.json(data);
    };
  var query={pesticideID:req.params.id};
  Pesticide.findOne(query,onSuccess);
});

apiRouter.post('/', function(req, res) {
  var onSuccess=function (err, data) {
   if (err) {console.log("error occured!");}
    res.json(data);
  };
   var newPesticide=req.body;
   Pesticide.create(newPesticide, onSuccess);
});

apiRouter.put('/:id', function(req, res) {
    var onSuccess=function (err, data) {
    if (err) {console.log("error occured!");}
    res.json(data);
    };
  var query={pesticideID:req.params.id};
  Pesticide.findOneAndUpdate(query, req.body,onSuccess);
});

apiRouter.delete('/:id', function(req, res) {
  
  var onSuccess=function (err, data) {
  if (err) {console.log("error occured!");}
    res.json(data);
  };

  var query={pesticideID:req.params.id};
  var thePesticide=req.body;
  Pesticide.findOneAndRemove(query,thePesticide,onSuccess);
});

module.exports = apiRouter;